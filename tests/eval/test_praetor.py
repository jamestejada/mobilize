DIRECTIVE_QUALITY_CRITERION = """
You are evaluating a research directive generated by a coordinator agent.
The directive should follow a strict format for routing OSINT research tasks.

PASS if ALL of the following are true:
- Contains an OBJECTIVE: section with one clear, specific sentence describing the research goal
- Contains a TASKS: section with a numbered list that includes at least one specific tool name
- Tool names referenced are valid OSINT tools from this list: search_web, search_news,
  search_bluesky_posts, get_bluesky_profile, get_author_feed, get_trending_topics,
  search_wikipedia, search_reddit_history, fetch_archived_page, fetch_url,
  list_gov_rss_feeds, get_gov_rss_feed, list_world_news_rss_feeds, get_world_news_rss_feed,
  get_protests_for_llm, search_polymarket, get_polymarket_event, search_candidate_finance,
  search_committee_finance, search_legislation, search_court_cases, get_registered_sources
- Contains a PRIORITY SOURCES: section
- The objective and tasks are specific to the user query (not generic boilerplate)
- The directive is at least 3 lines long

FAIL if ANY of the following are true:
- Missing OBJECTIVE:, TASKS:, or PRIORITY SOURCES: section
- The TASKS list contains no recognizable tool names
- The directive is fewer than 3 lines (incomplete)
- The objective is generic and not tied to the actual query content
"""

