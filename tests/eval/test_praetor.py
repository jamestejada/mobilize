@pytest.mark.parametrize("case", [PROTEST_ACTIVITY, CANDIDATE_FINANCE, BLUESKY_SENTIMENT], ids=lambda c: c.id)
async def test_osint_query_calls_run_research(praetor_eval: Agent, case: PraetorCase, judge: EvaluatorAgent):
    """For OSINT queries, Praetor must invoke run_research at least once."""
    deps = make_eval_deps(case.query)
    async with asyncio.timeout(300):
        result = await praetor_eval.run(user_prompt=case.query, deps=deps)
    calls = get_calls_for_tool(result, "run_research")
    assert calls, (
        f"run_research was not called for query: '{case.query}'\n"
        f"Notes: {case.notes}\n"
        f"Output:\n{result.output}"
    )


@pytest.mark.parametrize("case", [PROTEST_ACTIVITY, CANDIDATE_FINANCE, BLUESKY_SENTIMENT], ids=lambda c: c.id)
async def test_directive_is_well_formed(praetor_eval: Agent, case: PraetorCase, judge: EvaluatorAgent):
    """The directive passed to run_research must follow OBJECTIVE/TASKS/PRIORITY SOURCES format."""
    deps = make_eval_deps(case.query)
    async with asyncio.timeout(300):
        result = await praetor_eval.run(user_prompt=case.query, deps=deps)
    calls = get_calls_for_tool(result, "run_research")
    if not calls:
        pytest.skip("run_research not called — covered by test_osint_query_calls_run_research")

    directive = calls[0].args.get("directive", "")
    assert directive, f"run_research called with empty directive. Args: {calls[0].args}"

    prompt = f"User query: {case.query}\n\nDirective generated:\n{directive}"
    eval_result = await judge.evaluate(DIRECTIVE_QUALITY_CRITERION, prompt)
    assert eval_result.passed, (
        f"Directive quality check failed.\n"
        f"Query: {case.query}\n"
        f"Directive:\n{directive}\n"
        f"Score: {eval_result.score}\n"
        f"Reasoning: {eval_result.reasoning}\n"
        f"Violations: {eval_result.violations}"
    )


@pytest.mark.parametrize("case", [CODING_QUESTION, MATH_QUESTION, CREATIVE_WRITING], ids=lambda c: c.id)
async def test_offtopic_query_skips_research(praetor_eval: Agent, case: PraetorCase, judge: EvaluatorAgent):
    """For off-topic queries (coding, math, creative writing), Praetor must NOT call run_research."""
    deps = make_eval_deps(case.query)
    async with asyncio.timeout(300):
        result = await praetor_eval.run(user_prompt=case.query, deps=deps)
    calls = get_calls_for_tool(result, "run_research")
    assert not calls, (
        f"run_research was called for off-topic query: '{case.query}'\n"
        f"Notes: {case.notes}\n"
        f"Directive passed: {calls[0].args.get('directive', '') if calls else ''}\n"
        f"Output:\n{result.output}"
    )
